    private void filterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filterButtonActionPerformed
    	TableRowSorter<TfidfModel> sorter = new TableRowSorter<TfidfModel>(model);
    	table.setRowSorter(sorter);
    	
    	List<RowFilter<TfidfModel,Object>> filters = new ArrayList<RowFilter<TfidfModel,Object>>();
    	RowFilter<TfidfModel, Object> wordFilter = null;
    	RowFilter<TfidfModel, Object> docFilter = null;
    	
    	addFromToDoubleFilter(tfidfFrom, tfidfTo, filters, 2);
    	
    	RowFilter<TfidfModel, Object> compoundRowFilter = null;
    	
        //If current expression doesn't parse, don't update.
        try {
            wordFilter = RowFilter.regexFilter(word.getText(), 0);
            docFilter = RowFilter.regexFilter(document.getText(), 1);
            filters.add(wordFilter);
            filters.add(docFilter);
            compoundRowFilter = RowFilter.andFilter(filters);
        } catch (java.util.regex.PatternSyntaxException e) {
            return;
        }
        
        sorter.setRowFilter(compoundRowFilter);

    }//GEN-LAST:event_filterButtonActionPerformed
    
    private void addFromToDoubleFilter(JTextField from, JTextField to, 
    		List<RowFilter<TfidfModel,Object>> filters, int col) {
    	if (!from.getText().isEmpty()) {
    		RowFilter<TfidfModel, Object> filterFrom = RowFilter.numberFilter(ComparisonType.AFTER, Double.parseDouble(from.getText()), col);
    		filters.add(filterFrom);
    	}
    	if (!to.getText().isEmpty()) {
    		RowFilter<TfidfModel, Object> filterTo = RowFilter.numberFilter(ComparisonType.BEFORE, Double.parseDouble(to.getText()), col);
    		filters.add(filterTo);
    	}
    }

